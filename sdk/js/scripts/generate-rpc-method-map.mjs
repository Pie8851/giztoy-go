import { readFileSync } from "node:fs";
import { mkdir, writeFile } from "node:fs/promises";

const rpcProtoURL = new URL("../../../api/rpc/peer.proto", import.meta.url);
const outputURL = new URL("../gizclaw/generated/rpc/method-map.ts", import.meta.url);

const methods = parseRPCMethods(readFileSync(rpcProtoURL, "utf8"));

if (!Array.isArray(methods) || methods.length === 0) {
  throw new Error("api/rpc/peer.proto must define RpcMethod entries before generating RPC TypeScript.");
}

for (const item of methods) {
  if (typeof item.method !== "string" || typeof item.request !== "string" || typeof item.response !== "string" || !Number.isSafeInteger(item.id)) {
    throw new Error("invalid RpcMethod entry");
  }
}

const payloadTypes = Array.from(new Set(methods.flatMap((item) => [item.request, item.response]))).sort();
const imports = `import type { ${payloadTypes.join(", ")} } from "./payload-codec.ts";\n\n`;
const entries = methods
  .map((item) => `  ${JSON.stringify(item.method)}: {\n    request: ${item.request};\n    response: ${item.response};\n  };`)
  .join("\n");
const constants = methods.map((item) => `  ${JSON.stringify(item.method)}: ${JSON.stringify(item.method)},`).join("\n");
const ids = methods.map((item) => `  ${JSON.stringify(item.method)}: ${item.id},`).join("\n");

const text = `/* eslint-disable */\n// This file is generated by sdk/js/scripts/generate-rpc-method-map.mjs.\n// Do not edit by hand.\n\n${imports}export type RPCMethodMap = {\n${entries}\n};\n\nexport type RPCMethodName = keyof RPCMethodMap;\n\nexport const RPC_METHODS = {\n${constants}\n} as const satisfies Record<RPCMethodName, RPCMethodName>;\n\nexport const RPC_METHOD_IDS = {\n${ids}\n} as const satisfies Record<RPCMethodName, number>;\n`;

await mkdir(new URL(".", outputURL), { recursive: true });
await writeFile(outputURL, text);

function parseRPCMethods(proto) {
  const lines = proto.split(/\r?\n/);
  const methods = [];
  for (const line of lines) {
    const entry =
      /^\s*RPC_METHOD_[A-Z0-9_]+\s*=\s*(\d+)\s*\[\(rpc_method\)\s*=\s*\{\s*name:\s*"([^"]+)"\s+request:\s*"(\w+)"\s+response:\s*"(\w+)"\s*\}\s*\]\s*;/.exec(line);
    if (entry == null) {
      if (/^\s*RPC_METHOD_[A-Z0-9_]+\s*=/.test(line) && !line.includes("RPC_METHOD_UNSPECIFIED")) {
        throw new Error(`RpcMethod entry missing rpc_method option: ${line.trim()}`);
      }
      continue;
    }

    methods.push({ id: Number(entry[1]), method: entry[2], request: entry[3], response: entry[4] });
  }
  return methods;
}
