import { mkdir, writeFile } from "node:fs/promises";
import { readFileSync } from "node:fs";

const rpcSchemaURL = new URL("../../../api/rpc.json", import.meta.url);
const outputURL = new URL("../gizclaw/generated/rpc/method-map.ts", import.meta.url);

const schemaCache = new Map();
const schema = readJSON(rpcSchemaURL);
const methods = schema["x-gizclaw-rpc-methods"];

if (!Array.isArray(methods) || methods.length === 0) {
  throw new Error("api/rpc.json must define x-gizclaw-rpc-methods before generating RPC TypeScript.");
}

const typeNames = new Set();
for (const item of methods) {
  if (typeof item?.method !== "string" || typeof item?.request !== "string" || typeof item?.response !== "string") {
    throw new Error("invalid x-gizclaw-rpc-methods entry");
  }
  typeNames.add(resolveGeneratedTypeName(item.request, rpcSchemaURL));
  typeNames.add(resolveGeneratedTypeName(item.response, rpcSchemaURL));
}

const imports = [...typeNames].sort().join(",\n  ");
const entries = methods
  .map(
    (item) => `  ${JSON.stringify(item.method)}: {\n    request: ${resolveGeneratedTypeName(item.request, rpcSchemaURL)};\n    response: ${resolveGeneratedTypeName(item.response, rpcSchemaURL)};\n  };`,
  )
  .join("\n");
const constants = methods.map((item) => `  ${JSON.stringify(item.method)}: ${JSON.stringify(item.method)},`).join("\n");

const text = `/* eslint-disable */\n// This file is generated by sdk/js/scripts/generate-rpc-method-map.mjs.\n// Do not edit by hand.\n\nimport type {\n  ${imports}\n} from \"./types.gen.ts\";\n\nexport type RPCMethodMap = {\n${entries}\n};\n\nexport type RPCMethodName = keyof RPCMethodMap;\n\nexport const RPC_METHODS = {\n${constants}\n} as const satisfies Record<RPCMethodName, RPCMethodName>;\n`;

await mkdir(new URL(".", outputURL), { recursive: true });
await writeFile(outputURL, text);

function resolveGeneratedTypeName(ref, baseURL) {
  const resolved = resolveSchemaRef(ref, baseURL);
  const schema = resolved.schema;
  if (schema && Object.keys(schema).length === 1 && typeof schema.$ref === "string") {
    return resolveGeneratedTypeName(schema.$ref, resolved.documentURL);
  }
  return refTypeName(ref);
}

function resolveSchemaRef(ref, baseURL) {
  const marker = "#/components/schemas/";
  const index = ref.indexOf(marker);
  if (index < 0) {
    throw new Error(`unsupported schema ref: ${ref}`);
  }
  const documentURL = new URL(ref.slice(0, index) || baseURL.href, baseURL);
  const typeName = ref.slice(index + marker.length);
  const document = readJSON(documentURL);
  const schema = document.components?.schemas?.[typeName];
  if (schema == null) {
    throw new Error(`schema ref not found: ${ref}`);
  }
  return { documentURL, schema };
}

function refTypeName(ref) {
  const marker = "#/components/schemas/";
  const index = ref.indexOf(marker);
  if (index < 0) {
    throw new Error(`unsupported schema ref: ${ref}`);
  }
  return ref.slice(index + marker.length);
}

function readJSON(url) {
  const key = url.href;
  const cached = schemaCache.get(key);
  if (cached != null) {
    return cached;
  }
  const parsed = JSON.parse(readFileSync(url, "utf8"));
  schemaCache.set(key, parsed);
  return parsed;
}
