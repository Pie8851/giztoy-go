import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";

const protoURL = new URL("../../../api/telemetry/peer_telemetry.proto", import.meta.url);
const outputURL = new URL("../gizclaw/generated/telemetry/peer_telemetry.ts", import.meta.url);
const proto = readFileSync(protoURL, "utf8");
const messages = parseProtoMessages(proto);

for (const expected of [
  "TelemetryFrame",
  "Observation",
  "BatteryObservation",
  "GnssObservation",
  "NetworkObservation",
  "SystemObservation",
]) {
  if (!messages.has(expected)) {
    throw new Error(`api/telemetry/peer_telemetry.proto missing message ${expected}`);
  }
}

const text = `/* eslint-disable */
// This file is generated by sdk/js/scripts/generate-telemetry-protobuf.mjs.
// Do not edit by hand.

${generateTypes(messages)}

${generateEncoders(messages)}

class ProtoWriter {
  private readonly chunks: number[] = [];

  uint32(field: number, value: number): void {
    this.tag(field, 0);
    this.varint(BigInt(value >>> 0));
  }

  int32(field: number, value: number): void {
    this.tag(field, 0);
    this.varint(BigInt.asUintN(64, BigInt(value | 0)));
  }

  int64(field: number, value: number): void {
    this.tag(field, 0);
    this.varint(BigInt.asUintN(64, BigInt(Math.trunc(value))));
  }

  bool(field: number, value: boolean): void {
    this.tag(field, 0);
    this.varint(value ? 1n : 0n);
  }

  double(field: number, value: number): void {
    this.tag(field, 1);
    const data = new Uint8Array(8);
    new DataView(data.buffer).setFloat64(0, value, true);
    this.bytesRaw(data);
  }

  string(field: number, value: string): void {
    this.bytes(field, new TextEncoder().encode(value));
  }

  message(field: number, value: Uint8Array): void {
    this.bytes(field, value);
  }

  finish(): Uint8Array {
    return Uint8Array.from(this.chunks);
  }

  private tag(field: number, wireType: number): void {
    this.varint(BigInt((field << 3) | wireType));
  }

  private bytes(field: number, value: Uint8Array): void {
    this.tag(field, 2);
    this.varint(BigInt(value.length));
    this.bytesRaw(value);
  }

  private bytesRaw(value: Uint8Array): void {
    for (const byte of value) {
      this.chunks.push(byte);
    }
  }

  private varint(value: bigint): void {
    let next = value;
    while (next > 0x7fn) {
      this.chunks.push(Number((next & 0x7fn) | 0x80n));
      next >>= 7n;
    }
    this.chunks.push(Number(next));
  }
}
`;

mkdirSync(dirname(fileURLToPath(outputURL)), { recursive: true });
writeFileSync(outputURL, text);

function parseProtoMessages(source) {
  const clean = source.replace(/\/\/.*$/gm, "");
  const out = new Map();
  let index = 0;
  for (;;) {
    const match = /\bmessage\s+([A-Za-z_]\w*)\s*\{/g.exec(clean.slice(index));
    if (match == null) {
      return out;
    }
    const name = match[1];
    const open = index + match.index + match[0].length - 1;
    const close = findMatchingBrace(clean, open);
    const body = clean.slice(open + 1, close);
    out.set(name, parseMessage(name, body));
    index = close + 1;
  }
}

function parseMessage(name, body) {
  const fields = [];
  const oneofs = [];
  const lines = body.split(/\r?\n/);
  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i].trim();
    if (line === "") {
      continue;
    }
    const oneof = /^oneof\s+([A-Za-z_]\w*)\s*\{/.exec(line);
    if (oneof != null) {
      const oneofLines = [];
      for (i += 1; i < lines.length; i += 1) {
        const next = lines[i].trim();
        if (next === "}") {
          break;
        }
        oneofLines.push(next);
      }
      oneofs.push({
        fields: oneofLines.map((item) => parseField(item, { oneof: oneof[1] })),
        name: oneof[1],
      });
      continue;
    }
    fields.push(parseField(line));
  }
  return { fields, name, oneofs };
}

function parseField(line, options = {}) {
  const match = /^(?:(repeated|optional)\s+)?([A-Za-z_]\w*)\s+([A-Za-z_]\w*)\s*=\s*(\d+)\s*;/.exec(line);
  if (match == null) {
    throw new Error(`unsupported telemetry proto field: ${line}`);
  }
  return {
    fieldNumber: Number(match[4]),
    kind: match[1] ?? "",
    name: match[3],
    oneof: options.oneof,
    type: match[2],
  };
}

function findMatchingBrace(source, open) {
  let depth = 0;
  for (let i = open; i < source.length; i += 1) {
    if (source[i] === "{") {
      depth += 1;
    } else if (source[i] === "}") {
      depth -= 1;
      if (depth === 0) {
        return i;
      }
    }
  }
  throw new Error("unterminated telemetry proto message");
}

function generateTypes(messages) {
  const chunks = [];
  for (const message of messages.values()) {
    if (message.name === "Observation") {
      chunks.push(generateObservationType(message));
      continue;
    }
    chunks.push(generateMessageType(message));
  }
  return chunks.join("\n\n");
}

function generateMessageType(message) {
  const lines = [`export type ${message.name} = {`];
  for (const field of message.fields) {
    lines.push(`  ${camelCase(field.name)}${isTypeOptional(message.name, field) ? "?" : ""}: ${fieldType(field)};`);
  }
  lines.push("};");
  return lines.join("\n");
}

function generateObservationType(message) {
  const oneof = message.oneofs.find((item) => item.name === "body");
  if (oneof == null || oneof.fields.length === 0) {
    throw new Error("Observation must define oneof body");
  }
  const baseLines = ["export type ObservationBase = {"];
  for (const field of message.fields) {
    baseLines.push(`  ${camelCase(field.name)}?: ${fieldType(field)};`);
  }
  baseLines.push("};");
  const bodyKeys = oneof.fields.map((field) => camelCase(field.name));
  const variants = oneof.fields.map((field) => {
    const key = camelCase(field.name);
    const entries = bodyKeys.map((item) => `${item}${item === key ? "" : "?"}: ${item === key ? field.type : "never"}`);
    return `  | (ObservationBase & { ${entries.join("; ")} })`;
  });
  return `${baseLines.join("\n")}\n\nexport type Observation =\n${variants.join("\n")};`;
}

function generateEncoders(messages) {
  const chunks = [];
  for (const message of messages.values()) {
    chunks.push(generateEncoder(message, messages));
  }
  return chunks.join("\n\n");
}

function generateEncoder(message, messages) {
  const exportPrefix = message.name === "TelemetryFrame" ? "export " : "";
  const lines = [`${exportPrefix}function encode${message.name}(message: ${message.name}): Uint8Array {`, "  const writer = new ProtoWriter();"];
  for (const field of message.fields) {
    lines.push(...generateFieldEncoder(message.name, field, messages));
  }
  for (const oneof of message.oneofs) {
    lines.push(...generateOneofEncoder(oneof, messages));
  }
  lines.push("  return writer.finish();", "}");
  return lines.join("\n");
}

function generateOneofEncoder(oneof, messages) {
  const keys = oneof.fields.map((field) => camelCase(field.name));
  const lines = [
    `  const ${oneof.name}Count = ${keys.map((key) => `Number(message.${key} != null)`).join(" + ")};`,
    `  if (${oneof.name}Count !== 1) {`,
    `    throw new Error(\`telemetry observation must have exactly one body, got \${${oneof.name}Count}\`);`,
    "  }",
  ];
  for (const field of oneof.fields) {
    const key = camelCase(field.name);
    lines.push(`  if (message.${key} != null) {`);
    lines.push(`    writer.message(${field.fieldNumber}, encode${field.type}(message.${key}));`);
    lines.push("  }");
    if (!messages.has(field.type)) {
      throw new Error(`unsupported telemetry message field type ${field.type}`);
    }
  }
  return lines;
}

function generateFieldEncoder(messageName, field, messages) {
  const key = camelCase(field.name);
  const method = writerMethod(field.type, messages);
  if (field.kind === "repeated") {
    return [
      `  for (const item of message.${key} ?? []) {`,
      method === "message"
        ? `    writer.message(${field.fieldNumber}, encode${field.type}(item));`
        : `    writer.${method}(${field.fieldNumber}, item);`,
      "  }",
    ];
  }
  const writeLine = method === "message"
    ? `writer.message(${field.fieldNumber}, encode${field.type}(message.${key}));`
    : `writer.${method}(${field.fieldNumber}, message.${key});`;
  if (isTypeOptional(messageName, field)) {
    return [`  if (message.${key} != null) {`, `    ${writeLine}`, "  }"];
  }
  return [`  ${writeLine}`];
}

function fieldType(field) {
  const base = tsType(field.type);
  return field.kind === "repeated" ? `${base}[]` : base;
}

function isTypeOptional(messageName, field) {
  return field.kind === "optional" || field.kind === "repeated" || messageName === "TelemetryFrame" || messageName === "Observation";
}

function tsType(type) {
  switch (type) {
    case "uint32":
    case "int32":
    case "int64":
    case "double":
      return "number";
    case "bool":
      return "boolean";
    case "string":
      return "string";
    default:
      return type;
  }
}

function writerMethod(type, messages) {
  switch (type) {
    case "uint32":
    case "int32":
    case "int64":
    case "bool":
    case "double":
    case "string":
      return type;
    default:
      if (messages.has(type)) {
        return "message";
      }
      throw new Error(`unsupported telemetry proto type ${type}`);
  }
}

function camelCase(value) {
  return value.replace(/_([a-z])/g, (_, ch) => ch.toUpperCase());
}
