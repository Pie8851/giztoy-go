function parseRPCMethods(proto) {
  const lines = proto.split(/\r?\n/);
  const methods = [];
  for (const line of lines) {
    const entry =
      /^\s*RPC_METHOD_[A-Z0-9_]+\s*=\s*(\d+)\s*\[\(rpc_method\)\s*=\s*\{\s*name:\s*"([^"]+)"\s+request:\s*"(\w+)"\s+response:\s*"(\w+)"\s*\}\s*\]\s*;/.exec(line);
    if (entry == null) {
      if (/^\s*RPC_METHOD_[A-Z0-9_]+\s*=/.test(line) && !line.includes("RPC_METHOD_UNSPECIFIED")) {
        throw new Error(`RpcMethod entry missing rpc_method option: ${line.trim()}`);
      }
      continue;
    }
    methods.push({ id: Number(entry[1]), method: entry[2], request: entry[3], response: entry[4] });
  }
  return methods;
}

function parsePayloadProto(proto) {
  const lines = proto.split(/\r?\n/);
  const messages = {};
  const enums = {};
  let currentMessage = null;
  let currentEnum = null;
  let inOneof = false;

  for (const line of lines) {
    const enumStart = /^\s*enum\s+(\w+)\s*\{/.exec(line);
    if (enumStart != null) {
      currentEnum = { name: enumStart[1], values: [] };
      continue;
    }
    if (currentEnum != null) {
      if (/^\s*\}/.test(line)) {
        enums[currentEnum.name] = buildEnumDesc(currentEnum.name, currentEnum.values);
        currentEnum = null;
        continue;
      }
      const value = /^\s*([A-Z][A-Z0-9_]*)\s*=\s*(-?\d+)\s*;/.exec(line);
      if (value != null) {
        currentEnum.values.push({ name: value[1], number: Number(value[2]) });
      }
      continue;
    }

    const messageStart = /^\s*message\s+(\w+)\s*\{/.exec(line);
    if (messageStart != null) {
      currentMessage = { name: messageStart[1], fields: [] };
      continue;
    }
    if (currentMessage == null) {
      continue;
    }
    if (/^\s*oneof\s+value\s*\{/.test(line)) {
      inOneof = true;
      continue;
    }
    if (/^\s*\}/.test(line)) {
      if (inOneof) {
        inOneof = false;
        continue;
      }
      messages[currentMessage.name] = { fields: currentMessage.fields };
      currentMessage = null;
      continue;
    }
    const field = parseField(line, inOneof, currentMessage.name);
    if (field != null) {
      currentMessage.fields.push(field);
    }
  }
  return { messages, enums };
}

function parseField(line, oneof, messageName) {
  const map = /^\s*map<\s*string\s*,\s*([\w.]+)\s*>\s+(\w+)\s*=\s*(\d+)\s*(?:\[([^\]]*)\])?\s*;/.exec(line);
  if (map != null) {
    const name = fieldJSONName(map[2], map[4]);
    return {
      name,
      number: Number(map[3]),
      type: "map",
      mapValue: map[1],
      ...(OPTIONAL_MAP_FIELDS.has(`${messageName}.${name}`) ? { optional: true } : {}),
      ...(oneof ? { oneof: true } : {}),
    };
  }
  const match = /^\s*(optional\s+|repeated\s+)?([\w.]+)\s+(\w+)\s*=\s*(\d+)\s*(?:\[([^\]]*)\])?\s*;/.exec(line);
  if (match == null) {
    return null;
  }
  const name = fieldJSONName(match[3], match[5]);
  const repeated = match[1]?.trim() === "repeated";
  return {
    name,
    number: Number(match[4]),
    type: match[2],
    ...(repeated ? { repeated: true } : {}),
    ...(repeated && OPTIONAL_REPEATED_FIELDS.has(`${messageName}.${name}`) ? { optionalRepeated: true } : {}),
    ...(match[1]?.trim() === "optional" ? { optional: true } : {}),
    ...(oneof ? { oneof: true } : {}),
  };
}

function emitPayloadTypes(parsed) {
  const out = [];
  for (const name of Object.keys(parsed.enums).sort()) {
    out.push(`export type ${name} = ${enumTypeExpression(parsed.enums[name])};`);
  }
  for (const name of Object.keys(parsed.messages).sort()) {
    out.push(`export type ${name} = ${messageTypeExpression(name, parsed)};`);
  }
  return out.join("\n");
}

function enumTypeExpression(desc) {
  const values = new Set([""]);
  for (const name of Object.keys(desc.byName).sort()) {
    values.add(name);
  }
  return `${[...values].sort().map((value) => JSON.stringify(value)).join(" | ")} | number`;
}

function messageTypeExpression(name, parsed) {
  const desc = parsed.messages[name];
  if (desc == null) {
    return "Record<string, unknown>";
  }
  const single = singleValueTypeField(desc);
  if (single != null) {
    return tsFieldType(single, parsed);
  }
  if (desc.fields.length > 0 && desc.fields.every((field) => field.oneof === true)) {
    return desc.fields.map((field) => tsFieldType(field, parsed)).join(" | ") || "Record<string, never>";
  }
  if (desc.fields.length === 0) {
    return "Record<string, never>";
  }
  const lines = ["{"];
  for (const field of desc.fields) {
    if (field.oneof === true) {
      continue;
    }
    const optional = tsFieldOptional(field, parsed) ? "?" : "";
    lines.push(`  ${JSON.stringify(field.name)}${optional}: ${tsFieldType(field, parsed)};`);
  }
  lines.push("}");
  return lines.join("\n");
}

function singleValueTypeField(desc) {
  return desc.fields.length === 1 && desc.fields[0]?.name === "value" && desc.fields[0]?.oneof !== true ? desc.fields[0] : null;
}

function tsFieldOptional(field, parsed) {
  return field.optional === true || field.oneof === true || field.optionalRepeated === true;
}

function fieldJSONName(name, options) {
  const match = /(?:^|,)\s*json_name\s*=\s*"([^"]+)"/.exec(options ?? "");
  return match?.[1] ?? name;
}

function tsFieldType(field, parsed) {
  let type;
  if (field.mapValue != null) {
    type = `Record<string, ${tsType(field.mapValue, parsed)}>`;
  } else if (isOneofDiscriminatorField(field)) {
    type = "string";
  } else {
    type = tsType(field.type, parsed);
  }
  return field.repeated === true ? `${type}[]` : type;
}

function isOneofDiscriminatorField(field) {
  return (
    field.name === "agent_type" &&
    (
      field.type === "FlowcraftWorkspaceParametersAgentType" ||
      field.type === "DoubaoRealtimeWorkspaceParametersAgentType" ||
      field.type === "ASTTranslateWorkspaceParametersAgentType" ||
      field.type === "ChatRoomWorkspaceParametersAgentType"
    )
  );
}

function tsType(type, parsed) {
  switch (type) {
    case "bool":
      return "boolean";
    case "bytes":
    case "string":
      return "string";
    case "double":
    case "int32":
    case "int64":
    case "uint32":
    case "uint64":
      return "number";
    case "google.protobuf.Struct":
      return "Record<string, unknown>";
    case "google.protobuf.Value":
      return "unknown";
    default:
      if (parsed.enums[type] != null) {
        return type;
      }
      return parsed.messages[type] != null ? type : "unknown";
  }
}

function buildEnumDesc(_name, values) {
  const prefix = enumValuePrefix(values.map((item) => item.name));
  const byName = {};
  const byNumber = {};
  for (const item of values) {
    let jsonName = item.name;
    if (prefix !== "" && jsonName.startsWith(prefix)) {
      jsonName = jsonName.slice(prefix.length);
    }
    jsonName = enumJSONValue(jsonName);
    byName[jsonName] = item.number;
    byNumber[item.number] = item.number === 0 ? "" : jsonName;
  }
  return { byName, byNumber };
}

function enumJSONValue(name) {
  return ({
    "AST_TRANSLATE": "ast-translate",
    "DASH_SCOPE_TENANT": "dashscope-tenant",
    "DASHSCOPE_TENANT": "dashscope-tenant",
    "DOUBAO_REALTIME": "doubao-realtime",
    "EDGE_NODE": "edge-node",
    "GEMINI_TENANT": "gemini-tenant",
    "MINI_MAX": "minimax",
    "MINIMAX_TENANT": "minimax-tenant",
    "OPENAI_TENANT": "openai-tenant",
    "PUSH_TO_TALK": "push-to-talk",
    "VOLC_TENANT": "volc-tenant",
  })[name] ?? name.toLowerCase();
}

function enumValuePrefix(names) {
  if (names.length === 0) {
    return "";
  }
  let prefix = names[0];
  for (const name of names.slice(1)) {
    while (!name.startsWith(prefix) && prefix !== "") {
      prefix = prefix.slice(0, -1);
    }
  }
  const index = prefix.lastIndexOf("_");
  return index >= 0 ? prefix.slice(0, index + 1) : "";
}

function stableJSON(value) {
  return JSON.stringify(sortKeys(value), null, 2);
}

function sortKeys(value) {
  if (Array.isArray(value)) {
    return value.map(sortKeys);
  }
  if (value && typeof value === "object") {
    return Object.fromEntries(Object.entries(value).sort(([a], [b]) => a.localeCompare(b)).map(([key, item]) => [key, sortKeys(item)]));
  }
  return value;
}
